<template>
  <div>
    <user-search
      label="Add user..."
      :search-within-organization="searchUsersWithinOrganization"
      @selected="onUserAdd"
    />
    <v-simple-table dense>
      <template v-slot:default>
        <tbody>
          <users-table-row
            v-for="user in users"
            :user="user"
            :mode="mode"
            @change="onUserChanged"
            @delete="onUserDelete"
          />
        </tbody>
      </template>
    </v-simple-table>
  </div>
</template>

<script lang="ts">
import Vue, { PropOptions } from 'vue'
import UserSearch from '~/components/UserSearch.vue'
import UsersTableRow from '~/components/UsersTableRow.vue'
import {
  BaseUser,
  OrganizationUser,
  OrganizationUserList,
  ProjectUser,
  ProjectUserList,
} from '~/types'

enum Mode {
  Project = 'project',
  Organization = 'organization',
}

export default Vue.extend({
  components: {
    UserSearch,
    UsersTableRow,
  },
  props: {
    users: {
      type: Array,
      required: true,
    } as PropOptions<OrganizationUserList | ProjectUserList>,
    mode: {
      type: String,
      required: true,
    } as PropOptions<Mode>,
    searchUsersWithinOrganization: {
      type: Boolean,
      required: true,
    },
  },
  methods: {
    onUserAdd: function (user?: BaseUser) {
      if (user) {
        this.$emit('add', user)
      }
    },
    onUserChanged: function (user: ProjectUser | OrganizationUser) {
      this.$emit('change', user)
    },
    onUserDelete: function (user: ProjectUser | OrganizationUser) {
      this.$emit('delete', user)
    },
  },
})
</script>
